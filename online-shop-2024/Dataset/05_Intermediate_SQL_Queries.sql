-- Q18.
-- Find the Top 10 customers based on total spending.

SELECT
    c.customer_id,
    CONCAT(c.first_name, ' ', c.last_name) AS full_name,
    SUM(oi.quantity * oi.price_at_purchase) AS total_spending
FROM order_items AS oi
JOIN orders AS o
ON oi.order_id = o.order_id
JOIN customers AS c
ON o.customer_id = c.customer_id
GROUP BY
    c.customer_id,
    c.first_name,
    c.last_name
ORDER BY total_spending DESC
LIMIT 10;

-- -----------------------------------------------------------------

-- Q19.
-- Find the Top 10 best-selling products based on quantity sold.

SELECT 
	oi.product_id,
	p.product_name,
	SUM(oi.quantity) AS quantity_sold
FROM 
order_items AS oi
JOIN
products AS p
ON oi.product_id = p.product_id
GROUP BY 
	oi.product_id,
	p.product_name
ORDER BY quantity_sold DESC
LIMIT 10;

