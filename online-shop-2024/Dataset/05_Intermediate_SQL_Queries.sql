-- Q12.
-- Display each ordered product with its order ID, product name, quantity, and price at purchase.

SELECT
	oi.order_id,
	p.product_name,
	oi.quantity,
	oi.price_at_purchase
FROM order_items AS oi
JOIN products AS p
ON oi.product_id = p.product_id;

-- -----------------------------------------------------------------

-- Q13.
-- Calculate the total sales amount for each order using order_items.

SELECT 
	order_id,
	SUM(quantity * price_at_purchase) AS total_sale_amount
FROM order_items
GROUP BY order_id
ORDER BY total_sale_amount DESC;


-- -----------------------------------------------------------------

-- Q14.
-- Find the total quantity sold for each product.

SELECT 
	oi.product_id,
	p.product_name,
	SUM(oi.quantity) AS total_quantity_sold
FROM
order_items AS oi
JOIN
products AS p
ON oi.product_id = p.product_id
GROUP BY 
	oi.product_id,
	p.product_name;


-- -----------------------------------------------------------------

-- Q15.
-- Find the total revenue generated by each product.

SELECT
	oi.product_id,
	p.product_name,
	SUM(quantity * price_at_purchase) AS total_revenue
FROM
order_items AS oi
JOIN
products AS p
ON oi.product_id = p.product_id
GROUP BY 
	oi.product_id,
	p.product_name
ORDER BY total_revenue DESC;



-- -----------------------------------------------------------------

-- Q16.
-- Find the total revenue generated by each product category.

SELECT
	p.category,
	SUM(quantity * price_at_purchase) AS total_revenue
FROM
order_items AS oi
JOIN
products AS p
ON oi.product_id = p.product_id
GROUP BY 
	p.category
ORDER BY total_revenue DESC;



