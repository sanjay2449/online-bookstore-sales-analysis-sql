-- Q17.
-- Display the total number of orders placed by each customer.
-- Include customers who never placed an order.

SELECT 
	c.customer_id,
	CONCAT(c.first_name, ' ', c.last_name) AS full_name,
	COUNT(o.order_id) AS number_of_placed_orders
FROM 
customers AS c
LEFT JOIN
orders AS o
ON c.customer_id = o.customer_id
GROUP BY c.customer_id,full_name
ORDER BY number_of_placed_orders DESC


