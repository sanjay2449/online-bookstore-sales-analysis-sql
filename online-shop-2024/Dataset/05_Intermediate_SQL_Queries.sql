-- Q11.
-- Display each order along with the customer's full name and order date.

SELECT 
	o.order_id,
	concat(c.first_name, ' ', c.last_name) AS customer_full_name,
	o.order_date
FROM
orders AS o
JOIN
customers AS c
ON o.customer_id = c.customer_id



