-- Q38.
-- Calculate the running total of monthly revenue using SUM() OVER().

WITH monthly_sales AS
(
   SELECT
	TO_CHAR(o.order_date, 'YYYY-MM') AS month_number,
	SUM(oi.quantity * oi.price_at_purchase) AS monthly_sales_revenue
	FROM 
	order_items AS oi
	JOIN 
	orders AS o
	ON oi.order_id = o.order_id
	GROUP BY month_number
	ORDER BY month_number
)
SELECT
    month_number,
    monthly_sales_revenue,
    SUM(monthly_sales_revenue)
        OVER(ORDER BY month_number) AS running_total
FROM monthly_sales;

