-- Q37.
-- Find the top-selling product in each category using DENSE_RANK().

SELECT
*
FROM(
SELECT 
	p.product_id,
	p.product_name,
	p.category,
	SUM(oi.quantity) AS quantity_sold,
	DENSE_RANK() OVER (PARTITION BY p.category ORDER BY (SUM(oi.quantity)) DESC) AS order_rank
FROM
order_items AS oi
JOIN
products AS p
ON oi.product_id = p.product_id
GROUP BY 
	p.product_id,
	p.product_name,
	p.category
) t
WHERE order_rank = 1
ORDER BY category;

