-- Q32.
-- Using a CTE, calculate the total revenue generated by each product category and display only categories whose total revenue exceeds ₹50,000.
-- Display:
-- Category
-- Total Revenue

WITH revenue_by_category AS
(
SELECT
	p.category AS category,
	SUM(quantity * price_at_purchase) AS total_revenue
FROM
order_items AS oi
JOIN
products AS p
ON oi.product_id = p.product_id
GROUP BY 
	p.category
)

SELECT
	category,
	total_revenue
FROM 
revenue_by_category
WHERE total_revenue > 50000
ORDER BY total_revenue DESC





