-- Q31.
-- Using a CTE, calculate the total orders placed by each customer and display only customers who placed more than 5 orders.
-- Display:
-- Customer ID
-- Customer Name
-- Total Orders

WITH orders_placed_by_customers AS
(
    SELECT
        c.customer_id,
        CONCAT(c.first_name,' ',c.last_name) AS customer_name,
        COUNT(DISTINCT o.order_id) AS total_orders
    FROM orders AS o
    JOIN customers AS c
        ON o.customer_id = c.customer_id
    GROUP BY
        c.customer_id,
        c.first_name,
        c.last_name
)

SELECT
    customer_id,
    customer_name,
    total_orders
FROM orders_placed_by_customers
WHERE total_orders >= 2
ORDER BY total_orders DESC;
