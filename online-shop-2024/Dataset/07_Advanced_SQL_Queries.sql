	SUM(oi.quantity * oi.price_at_purchase) AS total_spending
FROM
order_items AS oi
JOIN
orders AS o
ON oi.order_id = o.order_id
JOIN
customers AS c
ON o.customer_id = c.customer_id
GROUP BY c.customer_id,customer_name
)
SELECT 
	customer_name,
	total_spending
FROM 
total_spending_by_customer
WHERE total_spending > 10000


