-- Q39.
-- Compare each month's revenue with the previous month using LAG().
	-- Display:
	-- Month
	-- Current Revenue
	-- Previous Month Revenue
	-- Revenue Difference

WITH monthly_sales AS
(
   SELECT
	TO_CHAR(o.order_date, 'YYYY-MM') AS month_number,
	SUM(oi.quantity * oi.price_at_purchase) AS monthly_sales_revenue
	FROM 
	order_items AS oi
	JOIN 
	orders AS o
	ON oi.order_id = o.order_id
	GROUP BY month_number
	ORDER BY month_number
)
SELECT
    month_number,
    monthly_sales_revenue,
    LAG(monthly_sales_revenue)
        OVER(ORDER BY month_number) AS previous_month_revenue,
	(monthly_sales_revenue
	-
	LAG(monthly_sales_revenue)
	OVER(ORDER BY month_number))
AS revenue_difference
FROM monthly_sales;

